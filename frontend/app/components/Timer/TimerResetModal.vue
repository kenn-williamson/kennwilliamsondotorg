<template>
  <div v-if="show" class="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center p-4 z-50">
    <div class="bg-white rounded-lg shadow-xl max-w-md w-full p-6">
      <h3 class="text-lg font-semibold text-gray-900 mb-4">Reset Incident Timer</h3>
      
      <form @submit.prevent="handleSubmit">
        <div class="mb-4">
          <label for="resetNotes" class="block text-sm font-medium text-gray-700 mb-2">
            Notes (optional)
          </label>
          <textarea
            id="resetNotes"
            v-model="form.notes"
            class="w-full px-3 py-2 border border-gray-300 rounded-md focus:ring-blue-500 focus:border-blue-500"
            rows="3"
            placeholder="Add any notes about this reset..."
          ></textarea>
        </div>
        
        <div class="flex gap-3 justify-end">
          <button
            type="button"
            @click="handleClose"
            class="px-4 py-2 text-gray-700 border border-gray-300 rounded-md hover:bg-gray-50 transition-colors duration-200"
          >
            Cancel
          </button>
          <button
            type="submit"
            :disabled="loading"
            class="px-4 py-2 bg-blue-600 text-white rounded-md hover:bg-blue-700 disabled:opacity-50 transition-colors duration-200"
          >
            {{ loading ? 'Resetting...' : 'Reset Timer' }}
          </button>
        </div>
      </form>
    </div>
  </div>
</template>

<script setup>
const props = defineProps({
  show: {
    type: Boolean,
    required: true
  },
  loading: {
    type: Boolean,
    default: false
  }
})

const emit = defineEmits(['close', 'reset'])

const form = reactive({
  notes: ''
})

const handleSubmit = () => {
  emit('reset', form.notes || undefined)
}

const handleClose = () => {
  form.notes = ''
  emit('close')
}

// Clear form when modal closes
watch(() => props.show, (isShowing) => {
  if (!isShowing) {
    form.notes = ''
  }
})
</script>