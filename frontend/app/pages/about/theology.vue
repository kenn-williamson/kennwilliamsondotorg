    <h3>What Is Mimetic Theory?</h3>

    <p>
      <strong>The core insight:</strong> Human desire is imitative (mimetic). We don't
      want things because they're inherently desirable to us. We want things because we
      see other people wanting them.
    </p>

    <p>
      A man sees his neighbor with a beautiful watch, suddenly he wants that watch. A
      child ignores a toy until another child picks it up, then suddenly it's the most
      important toy in the world. We learn what to desire by copying the desires of
      people around us.
    </p>

    <p>
      This leads to rivalry. If we're all imitating each other's desires, we end up
      wanting the same things, which means we end up competing for the same things.
      Competition breeds conflict.
    </p>

    <p>
      <strong>The scapegoat mechanism:</strong> When mimetic rivalry threatens to tear a
      community apart in all-against-all violence, societies have historically resolved
      the crisis through
      <SteampunkTooltip
        text="The scapegoat mechanism is society's method for dissipating tensions by redirecting violence to an outsider. When mimetic rivalry threatens the community, all participants spontaneously unite in violence against a single victim. This sacrifice (through death or expulsion) releases interpersonal tensions and restores social order. For it to work, participants must genuinely believe the victim is guilty—they cannot recognize it as scapegoating."
        placement="top"
      >
        <span>scapegoating</span> </SteampunkTooltip
      >. The community unites by projecting all their conflict onto a single victim.
      Former enemies become friends as they collectively participate in violence against
      a chosen scapegoat.
    </p>

    <p>
      This mechanism, repeated throughout human history, becomes ritualized as
      sacrifice. Ancient religions are built on this pattern: restore peace through the
      sacrifice of a victim. It works, temporarily, but it's based on a lie. The victim
      is innocent, chosen arbitrarily, but treated as if they're guilty and deserving of
      violence.
    </p>

    <p>
      <strong>The revelation:</strong> The Judeo-Christian scriptures, and especially
      the Gospels, expose this mechanism. Unlike every other ancient text that tells the
      story from the perspective of the mob, the Bible tells it from the perspective of
      the victim.
    </p>

