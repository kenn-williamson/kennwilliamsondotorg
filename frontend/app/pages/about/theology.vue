      scapegoated, even when it's uncomfortable.
    </p>

    <p>
      <strong>In work:</strong> When projects fail or problems arise, I try to look at
      systemic issues rather than blaming individuals. I try to create environments
      where people can grow rather than environments of fear and judgment.
    </p>

